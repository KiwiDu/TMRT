function sym_helper(ori) {
    return ori.split('_').map((s) => {
        if (s == 'l') return 'r'
        else if (s == 'r') return 'l'
        else return s
    }).join('_')
    /*
    e.g.    "l_wall_f" => [l, wall, f] => [r, wall, f] => "r_wall_f" 
    */
}


for (let part in body_parts) {
    if (part.startsWith('l_')) {
        let counterpart = sym_helper(part)
        let info = body_parts[part]
        for (let wall in info) {
            let sym_wall = sym_helper(wall)

            body_parts[counterpart][sym_wall] = body_parts[part][wall]
        }
    }
}

